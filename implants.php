          <h1>Имплантация зубов</h1>
          <div>
            <img src="img/implants/right-corner.png" width="226px" height="202px" class="img-right" style="width: auto;">
            <p>Имплантат – титановый аналог корня зуба, устанавливаемый в костную ткань верхней и нижней челюсти и фиксирующийся там за счет феномена остеоинтеграции.
            </p>
            <p>История имплантации начинается с 1965 года, когда Шведский ученый Пер Ингвар Бранемарк провел первую экспериментальную операцию по установке титанового имплантата собственного изготовления. Этот момент и считается открытием явления остеоинтеграции. Дальше последовали усиленные исследования, посвященные процессам приживления, фиксации и функционирования имплантата внутри костной ткани. В 1978 году Шведские власти одобрили использование дентальных имплантатов в клинических целях, тогда же профессор Бранемарк заключил соглашение о сотрудничестве с компанией Bofors AD о производстве и коммерческой реализации дентальных имплантатов.  В 1981 году образовалась дочерняя компания Bofors AD – Nobelpharma AD C 1996 года называющаяся Nobel Biocare и на сегодняшний день являющаяся лидером по производству дентальных имплантатов.   В 1982 году в Торонто состоялась первая конференция по Остеоинтеграции, где были представлены материалы 17 лет исследований.  С тех пор имплантация широко внедрилась в клиническую практику стоматологов всего мира и качественно меняет жизнь пациентов с потерей зубов
			</p>
			<p><b>Как выглядит конструкция на имплантате?</b></p>
			<p>Рассмотрим наиболее часто встречающийся вариант – восстановление одного зуба с помощью имплантата.</p>
			<p>Имплантат – титановый винт, устанавливаемый в костную ткань. Абатмент – головка, прикручиваемая к имплантату и являющаяся опорой для коронки. Коронка – Аналог своего зуба, повторяющий его по функции, форме, цвету и другим параметрам</p>
			<p>Это классическая трехкомпонентная конструкция, где абатмент прикручивается к имплантату, а коронка фиксируется на абатмент с помощью цемента.</p>
			<p>Существует второй вариант конструкции. В этом случае абатмент и коронка являются единым целым и фиксируются к имплантату с помощью винта (винтовая фиксация).</p>
			<p>У каждого из вариантов есть свои преимущества и недостатки и способ протезирования необходимо выбирать исходя из клинической ситуации.</p>
			<p><b>Кому можно устанавливать имплантаты?</b></p>
			<p>Как и у большинства медицинских процедур и манипуляций у имплантации есть противопоказания. Сюда включены:</p>
			<ul>
			<li>Эндокринные заболевания (сахарный диабет, тиреоидит и др.)</li>
			<li>Остеопороз</li>
			<li>Декомпенсированные (тяжело протекающие) заболевания жизненно важных систем организма</li>
			<li>Беременность и период лактации</li>
			<li>Неудовлетворительная гигиена полости рта</li>
			<li>Курение</li>
			</ul>
			<p>При наличии других заболеваний вам необходимо предупредить вашего лечащего врача и обсудить возможность проведения операции.</p>
			<p><b>Как подготовится к имплантации?</b></p>
			<p>Следует провести соответствующую диагностику:</p>
			<ul>
			<li>осмотр, опрос</li>
			<li>рентгеновское исследование (3д снимок)</li>
			<li>планирование лечение</li>
			</ul>
			<p>Для этого необходима консультации хирурга-имплантолога.</p>
			<p><b>Условия проведения процедуры имплантации</b></p>
			<ol>
			<li>Одно из основных условий для операции – наличие необходимого объема костной ткани как по высоте альвеолярного отростка, так и по ширине Минимальное расстояние по высоте 7-8 мм Минимальное расстояние по ширине 6-7мм</li>
			<li>Отсутствие очагов инфекции в непосредственной близости от зоны имплантации</li>
			<li>-разрушенные зубы, корни зубов -периодонтитные зубы -кисты, кистогранулемы -пародонтитные зубы, имеющие подвижность и воспалительный процесс в области зубодесневого прикрепления</li>
			<li>Отсутствие хронических, сезонных, простудных и вирусных заболеваний</li>
			<li>Удовлетворительная гигиена полости рта</li>
			</ol>
